    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $comment = new Comment();
        $comment->id_bus = $request->bus;
        $comment->id_user = $request->user;
        $comment->comment_text = $request->message;
        $comment->save();
        return $comment;
    }

    /**
     * Display the specified resource.
     *
     * @param  \App\Models\Comment  $comment
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $rus = DB::table('comments')->join('users', 'comments.id_user', '=', 'users.id')
        ->select('comments.comment_text','comments.created_at',"users.nom","users.prenom")
        ->where('id_bus', $id)
        ->limit(3)
        ->get();
        return $rus;
    }

